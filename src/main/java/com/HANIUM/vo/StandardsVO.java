package com.HANIUM.vo;

public class StandardsVO {
	private int id;
	private int code_type_no;
	private int upper_code_type_no;
	private String code_type_name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCode_type_no() {
		return code_type_no;
	}
	public void setCode_type_no(int code_type_no) {
		this.code_type_no = code_type_no;
	}
	public String getCode_type_name() {
		return code_type_name;
	}
	public void setCode_type_name(String code_type_name) {
		this.code_type_name = code_type_name;
	}
	public int getUpper_code_type_no() {
		return upper_code_type_no;
	}
	public void setUpper_code_type_no(int upper_code_type_no) {
		this.upper_code_type_no = upper_code_type_no;
	}
	
	
}
