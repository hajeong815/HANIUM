package com.HANIUM.vo;

public class StandardInsVO {
	private int id;
	private int code_type_no;
	private String item;
	private String content;
	
	public StandardInsVO() {}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCode_type_no() {
		return code_type_no;
	}
	public void setCode_type_no(int code_type_no) {
		this.code_type_no = code_type_no;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
}
