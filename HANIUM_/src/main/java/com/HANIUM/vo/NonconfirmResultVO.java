package com.HANIUM.vo;

public class NonconfirmResultVO {
	private int id;
	private String cda_type;
	private String cda_code;
	private String cda_name;
	private String content;
	private String solution;
	private String period;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCda_type() {
		return cda_type;
	}
	public void setCda_type(String cda_type) {
		this.cda_type = cda_type;
	}
	public String getCda_code() {
		return cda_code;
	}
	public void setCda_code(String cda_code) {
		this.cda_code = cda_code;
	}
	public String getCda_name() {
		return cda_name;
	}
	public void setCda_name(String cda_name) {
		this.cda_name = cda_name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getSolution() {
		return solution;
	}
	public void setSolution(String solution) {
		this.solution = solution;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	
	


}

