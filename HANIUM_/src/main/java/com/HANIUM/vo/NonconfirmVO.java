package com.HANIUM.vo;

public class NonconfirmVO {
	private int id;
	private String cda_code;
	private int noncon_ins_id;
	private String solution;
	private String noncon_period;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCda_code() {
		return cda_code;
	}
	public void setCda_code(String cda_code) {
		this.cda_code = cda_code;
	}

	public int getNoncon_ins_id() {
		return noncon_ins_id;
	}
	public void setNoncon_ins_id(int noncon_ins_id) {
		this.noncon_ins_id = noncon_ins_id;
	}
	public String getSolution() {
		return solution;
	}
	public void setSolution(String solution) {
		this.solution = solution;
	}
	public String getNoncon_period() {
		return noncon_period;
	}
	public void setNoncon_period(String noncon_period) {
		this.noncon_period = noncon_period;
	}


}

