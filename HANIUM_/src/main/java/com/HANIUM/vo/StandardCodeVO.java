package com.HANIUM.vo;

public class StandardCodeVO {
	private int id;
	private int standard_code;
	private int code_type_no;
	private String code_name;
	private int upper_standard_code;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getStandard_code() {
		return standard_code;
	}
	public void setStandard_code(int standard_code) {
		this.standard_code = standard_code;
	}
	public int getCode_type_no() {
		return code_type_no;
	}
	public void setCode_type_no(int code_type_no) {
		this.code_type_no = code_type_no;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public int getUpper_standard_code() {
		return upper_standard_code;
	}
	public void setUpper_standard_code(int upper_standard_code) {
		this.upper_standard_code = upper_standard_code;
	}
	
	
}
